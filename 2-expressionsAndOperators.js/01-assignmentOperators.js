//* Assignment Operator
let r = 5;

//* Addition Assignment
r += 1; //? r = r + 1

//* Subtraction Assignment
r -= 1; //? r = r - 1

//* Multiplication Assignment
r *= 1; //? r = r * 1

//* Division Assignment
r /= 1 //? r = r / 1

//* Remainder (modulus) Assignment
r %= 1 //? r = r % 1

//* Exponential Asignment
r **= 1 //? r = r ** 1


