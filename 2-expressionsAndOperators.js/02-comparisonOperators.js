//* Equal
console.log('3' == 3);  //? true

//* Strict Equal
console.log('3' === 3);  //? false

//* Not Equal
console.log('3' != 3);  //? false
 
//* Strict Not Equal
console.log('3' !== 3);  //? true

//* Greater than
3 > 2;

//* Less than
2 < 3;

//* Greater than or equal to
3 >= 2;

//* Less than or equal to
2 <= 3;

//* AND (true or false)
2 && 3;

//* OR (true or false)
2 || 3;

